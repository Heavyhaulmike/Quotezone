import { create } from "zustand";

const useFormStore = create((set) => ({
  step: 1,
  tripName: "",
  tripOrigin: "",
  tripDestination: "",
  length: "",
  width: "",
  height: "",
  weight: "",
  tripOriginLat: null,
  tripOriginLon: null,
  tripDestinationLat: null,
  tripDestinationLon: null,
  lengthInches: null,
  widthInches: null,
  heightInches: null,
  lowPrice: null,
  averagePrice: null,
  highPrice: null,
  inputValue: "",
  options: [],
  distance: null,
  setStep: (step) => set({ step }),
  setTripName: (name) => set({ tripName: name }),
  setTripOrigin: (origin) => set({ tripOrigin: origin }),
  setTripDestination: (destination) => set({ tripDestination: destination }),
  setLength: (length) => set({ length }),
  setWidth: (width) => set({ width }),
  setHeight: (height) => set({ height }),
  setWeight: (weight) => set({ weight }),
  setTripOriginLat: (lat) => set({ tripOriginLat: lat }),
  setTripOriginLon: (lon) => set({ tripOriginLon: lon }),
  setTripDestinationLat: (lat) => set({ tripDestinationLat: lat }),
  setTripDestinationLon: (lon) => set({ tripDestinationLon: lon }),
  setLengthInches: (inches) => set({ lengthInches: inches }),
  setWidthInches: (inches) => set({ widthInches: inches }),
  setHeightInches: (inches) => set({ heightInches: inches }),
  setInputValue: (value) => set({ inputValue: value }),
  setOptions: (options) => set({ options: options }),
  setLowPrice: (price) => set({ lowPrice: price }),
  setAveragePrice: (price) => set({ averagePrice: price }),
  setHighPrice: (price) => set({ highPrice: price }),
  setDistance: (distance) => set({ distance }),
  resetForm: () =>
    set({
      step: 1,
      tripName: "",
      tripOrigin: "",
      tripDestination: "",
      length: "",
      width: "",
      height: "",
      weight: "",
      tripOriginLat: null,
      tripOriginLon: null,
      tripDestinationLat: null,
      tripDestinationLon: null,
      lengthInches: 0,
      widthInches: 0,
      heightInches: 0,
      inputValue: "",
      options: [],
      lowPrice: null,
      averagePrice: null,
      highPrice: null,
      distance: null,
    }),
}));

export default useFormStore;
